import numpy

# input integers

x=int(input('Enter a number: x = '))
y=int(input('Enter a number: y = '))
out1=x**y
out2=numpy.log2(x)
print('x raised to the power y is',out1)
print('the log(base 2) of x is',out2)

# input float

