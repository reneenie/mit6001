import math

# input integers

x=int(input('Enter a number: x = '))
y=int(input('Enter a number: y = '))
out1=x**y
out2=math.log(x,2)
print('x raised to the power y is',out1)
print('the log(base 2) of x is',out2)

# input float

x=float(input('Enter a number: x = '))
y=float(input('Enter a number: y = '))
out1=x**y
out2=math.log(x,2)
print('x raised to the power y is',out1)
print('the log(base 2) of x is',out2)
